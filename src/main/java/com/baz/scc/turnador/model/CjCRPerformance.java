package com.baz.scc.turnador.model;

public class CjCRPerformance {
    
    private double tiempoConexion;
    private double tiempoEjecucion;
    private double tiempoIteracion;

    public double getTiempoConexion() {
        return tiempoConexion;
    }

    public void setTiempoConexion(double tiempoConexion) {
        this.tiempoConexion = tiempoConexion;
    }

    public double getTiempoEjecucion() {
        return tiempoEjecucion;
    }

    public void setTiempoEjecucion(double tiempoEjecucion) {
        this.tiempoEjecucion = tiempoEjecucion;
    }

    public double getTiempoIteracion() {
        return tiempoIteracion;
    }

    public void setTiempoIteracion(double tiempoIteracion) {
        this.tiempoIteracion = tiempoIteracion;
    }
    
}
